#Python program to show
#Bitwise operators
a = 10
b = 4  

#Bitwise Or(|) operator
print(a|b)

#Bitwise And(&) operator
print(a&b)

#Bitwise Xor(^) operator
print(a^b)    

#Bitwise Not(~) operator
print(~a)

#Shift operators
#Bitwise right shift operator
print(a>>1)
print(b>>1)

#Bitwise left shift operator
print(a<<1)
print(b<<1)